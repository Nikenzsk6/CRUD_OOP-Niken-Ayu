    void comboCategory() {
        class_Product product = new class_Product();
        ResultSet rs = product.comboCategory();
        
        try {
            while (rs.next()) {
                String category = rs.getString("categoryName");
                cKategori.addItem(category);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
        cKategori.setSelectedItem(0);
    }

    void loadTableProduct(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Product");
        model.addColumn("Nama Product");
        model.addColumn("Category");
        model.addColumn("Deskripsi Product");
        model.addColumn("Price");
        try {
            class_Product product = new class_Product();
            ResultSet result = product.showProduct();

            while (result.next()) {
                model.addRow(new Object[]{
                    result.getString("productId"),
                    result.getString("productname"),
                    result.getString("categoryName"),
                    result.getString("productDescription"),
                    result.getString("productPrice")
                });
            }
            table_product.setModel(model);

        } catch (SQLException e) {
            System.out.println("Error" + e.getMessage());
        }
    }
    
