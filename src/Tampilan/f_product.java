    private void table_productMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_productMouseClicked
        // TODO add your handling code here:
        int baris = table_product.rowAtPoint(evt.getPoint());
        
        String id = table_product.getValueAt(baris, 0).toString();
        String nama = table_product.getValueAt(baris, 1).toString();
        String category;
        String deskripsi = table_product.getValueAt(baris, 3).toString();
        String price = table_product.getValueAt(baris, 4).toString();
        
        //cek apakah kolom ke-2 (category kelas)
        if(table_product.getValueAt(baris, 2) !=null){
          category=table_product.getValueAt(baris, 2).toString();
        }else{
            category=null;
        }
        
        tIdProduct.setVisible(true);
        lblID.setVisible(true);
        
        tIdProduct.setText(id);
        tNamaProduct.setText(nama);
        cKategori.setSelectedItem(category);
        tDeskripsi.setText(deskripsi);
        tPrice.setText(price);
        
        tIdProduct.setEditable(false);
    }//GEN-LAST:event_table_productMouseClicked

