    private void b_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_tambahActionPerformed
        // set properti:
        class_User user = new class_User();
        user.setUserName(t_user.getText());
        user.setUserEmail(t_email.getText());
        user.setUserPassword(t_pw.getText());
        user.setUserFullName(t_fullname.getText());
        if (c_status.getSelectedItem() == "Active") {
            user.setUserStatus(1);
        } else {
            user.setUserStatus(0);
        }

        //panggil method tambah_user
        user.tambah_user();
        //reset inputan
        reset();
        load_table_user();
    }//GEN-LAST:event_b_tambahActionPerformed

    private void table_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_userMouseClicked
        // TODO add your handling code here:
        int baris = table_user.rowAtPoint(evt.getPoint());

        String username = table_user.getValueAt(baris, 0).toString();
        String email = table_user.getValueAt(baris, 1).toString();
        String fullname = table_user.getValueAt(baris, 2).toString();
        String status = table_user.getValueAt(baris, 3).toString();

        t_user.setText(username);
        t_email.setText(email);
        t_fullname.setText(fullname);
        c_status.setSelectedItem(status);

        t_user.setEditable(false);
    }//GEN-LAST:event_table_userMouseClicked

