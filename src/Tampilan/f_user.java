    void load_table_user() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Username");
        model.addColumn("Email");
        model.addColumn("Full Name");
        model.addColumn("Status");
        try {
            class_User user = new class_User();
            ResultSet result = user.showUser();

            while (result.next()) {
                String status = (result.getInt("userStatus") == 1) ? "Active" : "Inactive";
                model.addRow(new Object[]{
                    result.getString("userName"),
                    result.getString("userEmail"),
                    result.getString("userFullName"),
                    status
                });
            }
            table_user.setModel(model);

        } catch (SQLException e) {
            System.out.println("Error" + e.getMessage());
        }
    }

